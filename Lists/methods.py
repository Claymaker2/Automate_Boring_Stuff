#List methods are basically list specific functions 

#index method

x = ['blue','green', 'purple', 'red']

print(x.index('red')) #Will print out the index position of 'red'

#append method

x.append('kirko') #adds a value to the end of a list
print(x)

#insert method

x.insert(3,'bitch') #inserts a list entry at the designated index position